package edu.hbue.CPA.msg.domain;

public class Software {
	private int ID;
	private String AppName;
	private String CopyPath;
	private String InsPath;
	private int AppType;
	private String InsPara;
	/**
	 * @return the iD
	 */
	public int getID() {
		return ID;
	}
	/**
	 * @param iD the iD to set
	 */
	public void setID(int iD) {
		this.ID = iD;
	}
	/**
	 * @return the appName
	 */
	public String getAppName() {
		return AppName;
	}
	/**
	 * @param appName the appName to set
	 */
	public void setAppName(String appName) {
		this.AppName = appName;
	}
	/**
	 * @return the copyPath
	 */
	public String getCopyPath() {
		return CopyPath;
	}
	/**
	 * @param copyPath the copyPath to set
	 */
	public void setCopyPath(String copyPath) {
		this.CopyPath = copyPath;
	}
	/**
	 * @return the insPath
	 */
	public String getInsPath() {
		return InsPath;
	}
	/**
	 * @param insPath the insPath to set
	 */
	public void setInsPath(String insPath) {
		this.InsPath = insPath;
	}
	/**
	 * @return the appType
	 */
	public int getAppType() {
		return AppType;
	}
	/**
	 * @param appType the appType to set
	 */
	public void setAppType(int appType) {
		this.AppType = appType;
	}
	/**
	 * @return the insPara
	 */
	public String getInsPara() {
		return InsPara;
	}
	/**
	 * @param insPara the insPara to set
	 */
	public void setInsPara(String insPara) {
		this.InsPara = insPara;
	}
	
}
